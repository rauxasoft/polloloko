CREATE SEQUENCE "EMPLEADOS_SEQ" 
	MINVALUE 1000
	MAXVALUE 999999999 
	INCREMENT BY 50 
	START WITH 1000
	NOCACHE 
	NOCYCLE;
	
CREATE SEQUENCE "PRODUCTOS_SEQ" 
	MINVALUE 1000
	MAXVALUE 999999999 
	INCREMENT BY 50 
	START WITH 1000
	NOCACHE 
	NOCYCLE;
	
CREATE TABLE PRODUCTOS(

	CODIGO					BIGINT			NOT NULL,
	NOMBRE					VARCHAR(200)	,
	CATEGORIA				VARCHAR(100)	,
	DESCRIPCION				VARCHAR(250)	,
	PRECIO					DOUBLE			,
	FECHA_ALTA				DATE			,
	DESCATALOGADO			BOOLEAN			,
	
	PRIMARY KEY (CODIGO)
	
);
	

CREATE TABLE EMPLEADOS(

	CODIGO					BIGINT			NOT NULL,
	NOMBRE					VARCHAR(100)	,
	APELLIDO1				VARCHAR(100)	,
	APELLIDO2				VARCHAR(100)	,
	
	PRIMARY KEY(CODIGO)

);

